class Solution:
    """
    @param A: An integers array.
    @return: return any of peek positions.
    """
    def findPeak(self, A):
        n = len(A)
        l, r = 0, n - 1
        while l < r:
            m = l + ((r - l) >> 1)
            left, mid, right = A[m - 1], A[m], A[m + 1]
            if left < mid and right < mid:
                return m
            elif left > mid and right > mid:
                l = m + 1
            elif left < mid < right:
                l = m
            elif left > mid > right:
                r = m
        return l

